int fb_init(struct limine_framebuffer_response *response) {
    if (!response || response->framebuffer_count == 0) return 0;
    struct limine_framebuffer *fb = response->framebuffers[0];
    g_fb.addr   = (uint32_t *)fb->address;
    g_fb.width  = (uint32_t)fb->width;
    g_fb.height = (uint32_t)fb->height;
    g_fb.pitch  = (uint32_t)fb->pitch;
    g_fb.bpp    = (uint8_t)fb->bpp;
    return 1;
}

void fb_clear(uint32_t color) {
    for (uint32_t y = 0; y < g_fb.height; y++)
        for (uint32_t x = 0; x < g_fb.width; x++)
            g_fb.addr[(y * g_fb.pitch / 4) + x] = color;
}

void fb_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x >= g_fb.width || y >= g_fb.height) return;
    g_fb.addr[(y * g_fb.pitch / 4) + x] = color;
}

void fb_fill_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    uint32_t x2 = x + w; if (x2 > g_fb.width)  x2 = g_fb.width;
    uint32_t y2 = y + h; if (y2 > g_fb.height) y2 = g_fb.height;
    for (uint32_t row = y; row < y2; row++)
        for (uint32_t col = x; col < x2; col++)
            g_fb.addr[(row * g_fb.pitch / 4) + col] = color;
}

void fb_draw_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    fb_draw_hline(x,         y,         w, color);
    fb_draw_hline(x,         y + h - 1, w, color);
    fb_draw_vline(x,         y,         h, color);
    fb_draw_vline(x + w - 1, y,         h, color);
}

void fb_draw_hline(uint32_t x, uint32_t y, uint32_t len, uint32_t color) {
    for (uint32_t i = 0; i < len && x + i < g_fb.width; i++)
        g_fb.addr[(y * g_fb.pitch / 4) + x + i] = color;
}

void fb_draw_vline(uint32_t x, uint32_t y, uint32_t len, uint32_t color) {
    for (uint32_t i = 0; i < len && y + i < g_fb.height; i++)
        g_fb.addr[((y + i) * g_fb.pitch / 4) + x] = color;
}

void fb_put_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg) {
    uint8_t idx = (uint8_t)c;
    if (idx < 32 || idx > 127) idx = '?';
    idx -= 32;
    for (uint32_t row = 0; row < FONT_H; row++) {
        uint8_t bits = font_data[idx][row];
        for (uint32_t col = 0; col < FONT_W; col++)
            fb_put_pixel(x + col, y + row, (bits & (0x80 >> col)) ? fg : bg);
    }
}

void fb_print_at(uint32_t x, uint32_t y, const char *str, uint32_t fg, uint32_t bg) {
    uint32_t cx = x;
    while (*str) {
        if (*str == '\n') { cx = x; y += FONT_H; }
        else { fb_put_char(cx, y, *str, fg, bg); cx += FONT_W; }
        str++;
    }
}

