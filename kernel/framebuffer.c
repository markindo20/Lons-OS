static uint32_t con_rx=CON_PAD, con_ry=CON_PAD, con_rw=0, con_rh=0;
static uint32_t con_x=CON_PAD,  con_y=CON_PAD;
static uint32_t con_fg=0x00FFFFFF, con_bg=0x00000000;

void fb_console_init(void) {
    con_rx=CON_PAD; con_ry=CON_PAD;
    con_rw=g_fb.width-CON_PAD*2; con_rh=g_fb.height-CON_PAD*2;
    con_x=con_rx; con_y=con_ry;
    fb_clear(con_bg);
}

void fb_console_init_region(uint32_t x, uint32_t y, uint32_t w, uint32_t h) {
    con_rx=x; con_ry=y; con_rw=w; con_rh=h;
    con_x=x;  con_y=y;
    fb_fill_rect(x, y, w, h, con_bg);
}

void fb_console_set_color(uint32_t fg, uint32_t bg) { con_fg=fg; con_bg=bg; }

static void con_scroll(void) {
    uint32_t rpx=g_fb.pitch/4, bot=con_ry+con_rh;
    for (uint32_t y=con_ry; y<bot-FONT_H; y++) {
        uint32_t *dst=g_fb.addr+y*rpx+con_rx;
        uint32_t *src=g_fb.addr+(y+FONT_H)*rpx+con_rx;
        for (uint32_t x=0; x<con_rw; x++) dst[x]=src[x];
    }
    for (uint32_t y=bot-FONT_H; y<bot; y++) {
        uint32_t *row=g_fb.addr+y*rpx+con_rx;
        for (uint32_t x=0; x<con_rw; x++) row[x]=con_bg;
    }
    if (con_y>=con_ry+FONT_H) con_y-=FONT_H;
}

static void con_newline(void) {
    con_x=con_rx; con_y+=FONT_H;
    if (con_y+FONT_H>con_ry+con_rh) con_scroll();
}

void fb_console_print(const char *str) {
    while (*str) {
        if (*str=='\n') con_newline();
        else {
            fb_put_char(con_x, con_y, *str, con_fg, con_bg);
            con_x+=FONT_W;
            if (con_x+FONT_W>con_rx+con_rw) con_newline();
        }
        str++;
    }
}

void fb_console_backspace(void) {
    if (con_x<=con_rx && con_y<=con_ry) return;
    if (con_x>con_rx) {
        con_x-=FONT_W;
    } else {
        con_y-=FONT_H;
        con_x=con_rx+((con_rw/FONT_W)-1)*FONT_W;
    }
    fb_fill_rect(con_x, con_y, FONT_W, FONT_H, con_bg);
}

void fb_console_print_hex(uint64_t v) {
    char buf[19]; const char *h="0123456789ABCDEF";
    buf[0]='0'; buf[1]='x'; buf[18]=0;
    for (int i=15; i>=2; i--) { buf[i]=h[v&0xF]; v>>=4; }
    fb_console_print(buf);
}

void fb_console_print_dec(uint64_t v) {
    char buf[21]; int i=19; buf[20]=0;
    if (!v) { fb_console_print("0"); return; }
    while (v&&i>0) { buf[--i]=(char)('0'+(v%10)); v/=10; }
    fb_console_print(&buf[i]);
}