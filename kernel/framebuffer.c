/* ─────────────────────────────────────────────
 * fb_init
 * ───────────────────────────────────────────── */
int fb_init(struct limine_framebuffer_response *response) {
    if (!response || response->framebuffer_count == 0) return 0;

    struct limine_framebuffer *fb = response->framebuffers[0];

    g_fb.addr   = (uint32_t *)fb->address;
    g_fb.width  = (uint32_t)fb->width;
    g_fb.height = (uint32_t)fb->height;
    g_fb.pitch  = (uint32_t)fb->pitch;
    g_fb.bpp    = (uint8_t)fb->bpp;

    return 1;
}

/* ─────────────────────────────────────────────
 * Pixel drawing
 * ───────────────────────────────────────────── */
void fb_clear(uint32_t color) {
    for (uint32_t y = 0; y < g_fb.height; y++) {
        for (uint32_t x = 0; x < g_fb.width; x++) {
            /* pitch is in bytes, our pointer is uint32_t (4 bytes) */
            g_fb.addr[(y * g_fb.pitch / 4) + x] = color;
        }
    }
}

void fb_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x >= g_fb.width || y >= g_fb.height) return;
    g_fb.addr[(y * g_fb.pitch / 4) + x] = color;
}

/* ─────────────────────────────────────────────
 * Character rendering
 * ───────────────────────────────────────────── */
void fb_put_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg) {
    /* Map char to font table index */
    uint8_t idx = (uint8_t)c;
    if (idx < FONT_FIRST || idx > FONT_LAST) idx = '?';
    idx -= FONT_FIRST;

    for (uint32_t row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = font_data[idx][row];
        for (uint32_t col = 0; col < FONT_WIDTH; col++) {
            /* Bit 7 = leftmost pixel */
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_put_pixel(x + col, y + row, color);
        }
    }
}

void fb_print_at(uint32_t x, uint32_t y, const char *str, uint32_t fg, uint32_t bg) {
    uint32_t cx = x;
    while (*str) {
        if (*str == '\n') {
            cx = x;
            y += FONT_HEIGHT;
        } else {
            fb_put_char(cx, y, *str, fg, bg);
            cx += FONT_WIDTH;
        }
        str++;
    }
}

/* ─────────────────────────────────────────────
 * Scrolling text console
 *
 * Tracks a cursor position. On newline or when the
 * cursor reaches the right edge, wraps to next line.
 * When the cursor reaches the bottom, scrolls up.
 * ───────────────────────────────────────────── */

/* Padding around the console area */
#define CON_PAD_X 8
#define CON_PAD_Y 8

static uint32_t con_x   = CON_PAD_X;
static uint32_t con_y   = CON_PAD_Y;
static uint32_t con_fg  = FB_WHITE;
static uint32_t con_bg  = FB_BLACK;

/* How many chars fit per row/col */
static uint32_t con_cols = 0;
static uint32_t con_rows = 0;

void fb_console_init(void) {
    con_cols = (g_fb.width  - CON_PAD_X * 2) / FONT_WIDTH;
    con_rows = (g_fb.height - CON_PAD_Y * 2) / FONT_HEIGHT;
    con_x = CON_PAD_X;
    con_y = CON_PAD_Y;
    fb_clear(FB_BLACK);
}

void fb_console_set_color(uint32_t fg, uint32_t bg) {
    con_fg = fg;
    con_bg = bg;
}

/*
 * con_scroll — shift all pixels up by one line.
 * Copies rows 1..n to rows 0..n-1, clears the last row.
 */
static void con_scroll(void) {
    uint32_t row_pixels = g_fb.pitch / 4;  /* pixels per row in memory */
    uint32_t line_h     = FONT_HEIGHT;
    uint32_t top        = CON_PAD_Y;
    uint32_t bottom     = con_rows * FONT_HEIGHT + CON_PAD_Y;

    /* Move everything up by one text line */
    for (uint32_t y = top; y < bottom - line_h; y++) {
        uint32_t *dst = g_fb.addr + y             * row_pixels;
        uint32_t *src = g_fb.addr + (y + line_h)  * row_pixels;
        for (uint32_t x = 0; x < g_fb.width; x++) {
            dst[x] = src[x];
        }
    }

    /* Clear the last line */
    for (uint32_t y = bottom - line_h; y < bottom; y++) {
        uint32_t *row = g_fb.addr + y * row_pixels;
        for (uint32_t x = 0; x < g_fb.width; x++) {
            row[x] = con_bg;
        }
    }

    /* Move cursor up one line */
    if (con_y >= CON_PAD_Y + FONT_HEIGHT)
        con_y -= FONT_HEIGHT;
}

static void con_newline(void) {
    con_x  = CON_PAD_X;
    con_y += FONT_HEIGHT;
    if (con_y + FONT_HEIGHT > con_rows * FONT_HEIGHT + CON_PAD_Y) {
        con_scroll();
    }
}

void fb_console_print(const char *str) {
    while (*str) {
        if (*str == '\n') {
            con_newline();
        } else {
            fb_put_char(con_x, con_y, *str, con_fg, con_bg);
            con_x += FONT_WIDTH;
            /* Wrap at right edge */
            if (con_x + FONT_WIDTH > g_fb.width - CON_PAD_X) {
                con_newline();
            }
        }
        str++;
    }
}

void fb_console_print_hex(uint64_t value) {
    char buf[19];
    const char *hex = "0123456789ABCDEF";
    buf[0] = '0'; buf[1] = 'x'; buf[18] = '\0';
    for (int i = 15; i >= 2; i--) { buf[i] = hex[value & 0xF]; value >>= 4; }
    fb_console_print(buf);
}

void fb_console_print_dec(uint64_t value) {
    char buf[21]; int i = 19; buf[20] = '\0';
    if (value == 0) { fb_console_print("0"); return; }
    while (value > 0 && i > 0) { buf[--i] = (char)('0' + (value % 10)); value /= 10; }
    fb_console_print(&buf[i]);
}